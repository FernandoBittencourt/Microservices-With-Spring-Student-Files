package com.example.lab6.domain;

public class Word {
	private String word;

	public Word() {
	}	
	
	public Word(String word) {
		this();
		this.word = word;
	}
	
	public String getWord() {
		return word;
	}
	
	public void setWord(String word) {
		this.word = word;
	}
	
	public String getString() {
		return word;
	}
}
